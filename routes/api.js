const router = require("express").Router();
const Endurance = require("../models/endurance.js");
const Strength = require ("../models/strength.js")

router.post("/api/endurance", ({ body }, res) => {
  Endurance.create(body)
    .then(dbWorkout => {
      res.json(dbWorkout);
    })
    .catch(err => {
      res.status(400).json(err);
    });
});


